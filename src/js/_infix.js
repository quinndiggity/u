_infix = {
  '+': (a, b) => a + b,
  '-': (a, b) => a - b,
  '*': (a, b) => a * b,
  '/': (a, b) => a / b,
  '%': (a, b) => a % b,
  '=': (a, b) => a == b,
  '≠': (a, b) => a != b,
  '<': (a, b) => a < b,
  '>': (a, b) => a > b,
  '≤': (a, b) => a <= b,
  '≥': (a, b) => a >= b,
  '&': (a, b) => a && b,
  '|': (a, b) => a || b,
  '^': (a, b) => a.slice(b),
}
